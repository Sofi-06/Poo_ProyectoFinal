    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (estaTodoBien()) {
            String nombre, dane;

            nombre = cajaNombre.getText();
            dane = cajaDane.getText();

            objActualizar.setNombreDepartamento(nombre);
            objActualizar.setDaneDepartamento(dane);

            DaoDepartamento miDaoDep = new DaoDepartamento();

            if (miDaoDep.actualizar(objActualizar)) {
                JOptionPane.showMessageDialog(panelCuerpo, "Edici√≥n exitosa", "Informaci√≥n", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(panelCuerpo, "No se pudo editar", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_btnEditarActionPerformed

